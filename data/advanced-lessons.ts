function advancedStarter(seed: AdvancedSeed) {
  const headers = Array.from(new Set(
    seed.code
      .split("\n")
      .filter((line) => line.startsWith("#include")),
  ));
  return [
    ...(headers.length ? headers : ["#include <stdio.h>"]),
    "",
    "int main(void) {",
    "    /* 「書き方ガイド」のSTEP 1から、自分で実装しましょう。 */",
    "",
    "    return 0;",
    "}",
  ].join("\n");
}

function stageFor(id: AdvancedStageId) {
  return advancedStages.find((stage) => stage.id === id)!;
}

